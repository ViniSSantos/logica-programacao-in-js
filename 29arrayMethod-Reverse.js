var umAcinco = [1,2,3,4,5];

for(i=0; i < umAcinco.length; i++){
    console.log(umAcinco[i]);
}

var umAcincoReverse = umAcinco.reverse()
console.log(umAcincoReverse);
//->(5) [5, 4, 3, 2, 1]

for(i=0; i < umAcincoReverse.length; i++){
    console.log(umAcincoReverse[i]);
}
